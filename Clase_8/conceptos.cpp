/*
CONCEPTOS IMPORTANTES QUE DEBEMOS APRENDER

1. Una funcion tiene una tarea especifica
    NO conviene hacer todo dentro del main.

2. Parámetros
    Los parametros son lo datos que una funcion recibe
    Ejemplo:
    int calcularSuma(int a, int b, int c,...) TODO LO QUE ESTE DENTRO
    DE LOS PARENTESIS SON PARAMETROS

3. Retorno
    El retorno es el valor que la funcion devuelve
    Ejemplo:
        return a+b

4. Void
    Una funcion void no devuelve valor.

*/